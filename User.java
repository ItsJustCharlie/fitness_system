package org.example.fitnessproject;


public class User {
    private String empName;
    private int empId;





    public User(String empName, int empId) {
        this.empName = empName;
        this.empId = empId;

    }

    public String getEmpName() {
        return empName;
    }

    public int getEmpId() {
        return empId;
    }




}
